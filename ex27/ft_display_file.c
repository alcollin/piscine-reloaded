#include <unistd.h>
#include <fcntl.h>

void	display_file(int fd)
{
	char	buff;

	while (read(fd, &buff, 1))
		write(1, &buff, 1);
}

int		main(int argc, char **argv)
{
	int		fd;

	if (argc != 2)
	{
		if (argc > 2)
			write(2, "Too many arguments.\n", 20);
		if (argc < 2)
			write(2, "File name missing.\n", 19);
		return (1);
	}
	fd = open(argv[1], O_RDONLY);
	display_file(fd);
	close(fd);
	return (1);
}
